package day47_Encapsulation;

public class Encapsulation {

    private long ssn = 1234567;
    private String name = "";

    public long getSsn() {
        return ssn;
    }

    public void setSsn(long ssn) {
        this.ssn = ssn;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
}
